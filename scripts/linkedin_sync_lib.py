def read_source_payload(path: Path) -> FetchResult:
    raw_text = path.read_text(encoding="utf-8")
    fetched_at = dt.datetime.now(dt.UTC).replace(microsecond=0).isoformat()

    if path.suffix.lower() == ".json":
        raw_data = json.loads(raw_text)
        html = raw_data.get("html") or ""
        profile_url = raw_data.get("url") or raw_data.get("final_url") or ""
        final_url = raw_data.get("final_url") or profile_url
        status_code = int(raw_data.get("status_code") or 200)
        fetched_at = raw_data.get("fetched_at") or fetched_at
        return FetchResult(
            url=profile_url,
            final_url=final_url,
            status_code=status_code,
            fetched_at=fetched_at,
            html=html,
        )

    return FetchResult(
        url="",
        final_url="",
        status_code=200,
        fetched_at=fetched_at,
        html=raw_text,
    )
